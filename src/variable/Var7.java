package variable;

public class Var7 {

    public static void main(String[] args) {
        // Variable Types
        // Literal: A value that directly assigned to a variable
        int a = 100; // whole number
        double b = 10.5; // real number
        boolean c = true; // true, false
        char d = 'A'; // character
        String e = "Hello Java"; // string

        // int z = "100"; // Error: String cannot be converted to int

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
    }
}
