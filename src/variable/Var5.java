package variable;

public class Var5 {

    public static void main(String[] args) {
        //1. Declare w/o initialisation
        int a;
        a = 1;
        System.out.println(a);

        //2. Declare w/ initialisation
        int b = 2;
        System.out.println(b);

        //3. Multiple variables
        int c = 3, d = 4;
        System.out.println(c);
        System.out.println(d);
    }
}
